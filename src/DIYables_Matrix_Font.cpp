const uint8_t* DIYables_Font::getChar(unsigned char c) {
    if (c >= 32 && c <= 126) {
        return font[c - 32];
    } else if (c == 176) {  // ASCII code 176 for 'Â°'
        return degreeSymbol;
    } else {
        return defaultChar;
    }
}